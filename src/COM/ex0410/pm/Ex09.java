package COM.ex0410.pm;

public class Ex09 {

	public static void main(String[] args) {
		//if ~ else 
		/*
		 * if 조건식 뒤에 else와 해당 문장을 추가한 형태
		 * 조건이 참일시 if문 안의 문장이 실행되고 거짓일 시 else문 안의 문장이 실행된다.
		 * 
		 */
		if(5>3) {
			System.out.println("5는 3보다 큽니다.");
		}
		else{
			System.out.println("므ㅗ마ㅗ마");
		}
		

	}

}
