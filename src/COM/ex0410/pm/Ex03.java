package COM.ex0410.pm;

public class Ex03 {

	public static void main(String[] args) {
		
		int x = 3;
		int y = 4;
		
		System.out.println(x<y);
		System.out.println(x>y);
		System.out.println(x>=y);
		System.out.println(x<=y);
		System.out.println(x==y);
		System.out.println(x!=y);
		
		// 비교연산자
		/*
		 * 두 수의 대소 비교를 통해 참인지 거짓인지를 판단하는 연산자
		 * 비교 연산자는 총 6가지가 있으며 > , < , <= , >= , == , != 가 있다.
		 * 기본형 중에서는 boolean을 제외한 나머지 자료형에 전부 사용되지만 참조형 변수에는 사용할 수 없다.
		 * 
		 * 
		 */
	}

}
