package COM.ex0410.pm;

public class Ex15 {

	public static void main(String[] args) {
		
		/*
		 * do~ while문
		 * 수행 문장이 실행되고 난 뒤에 while안의 조건식이 실행되는 형태
		 * while문과 동일하지만 차이점은 무조건 한번 로직이 실행된다.
		 * 
		 */
		
		int x =6;
		do {
			System.out.println("안녕하세요 반갑습니다.");
			x++;
		}
		while(x<=5);

	}

}
